package game;

public class Colore {
	private final String colore;
	/**
	 * the construct of the class Colore set the variable colore at the parameter passed
	 * @param colore
	 */
	public Colore(String colore){
	
		this.colore=colore;
	}
	/**
	 * return the value of the variable colore
	 * @return colore 
	 */
	public String getColore() {
		return colore;
	}

}
